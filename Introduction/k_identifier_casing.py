"""
Purpose: Identifier Casing - class names should be CamelCase, identifier names should be in snake( or Underscore) case .

"""

# Python - case-sensitive language
animal = "CAT"
print(animal)

# print(Animal) - NameError: name 'Animal' is not defined.

ANIMAL = "RAT"
print(ANIMAL)

Animal = "Goat"
print(Animal)

